<h2>Mr Aakash Sharma</h2>
<p>(Director)</p>

<p>Education is for the total development of a child. Learning takes place in all walks of life, directly sometimes and sometimes indirectly.What and how much learning has taken place needs a system of evaluation to determine which can be subjective,or objective; qualitative or quantitative.</p>

<p>The society earlier gave importance to learning than to evaluation. It was very clearly and correctly understood by our education planners in particular and society in general that learning was the chief aim, the goal whereas evaluation was only the tool to decide whether learning had taken place.</p>

<p>Gradually an undesired change paved its way. Our society started giving more importance to evaluations i.e. the total marks attained over knowledge. Consequently evaluation which was the only tool, got an upper hand and learning was pushed to the back seat.</p>

<p>Time has come when the parents, the teachers, the members of society must understand that marks are not the criteria for the intelligence of a child. Let’s understand that if proper and effective learning has taken place, marks are bound to bounce. A true person is one who is multi-faceted, who excels academically, has wide interests, is adorned with physical and spiritual fitness that allows him to move forward. Our school focuses on the development of art, culture, character building and discipline.</p>

<p>"When people come together as a team, share a dream and focus on enriching their goal- a lot is accomplished". This journey has involved a vast canvas of highly innovative and motivated individuals - the parents, teachers and students.</p>

<p>We also need to educate our loving children that competition should always be with one’s own self. Students need to be given ample positive energy so as to indulge in self analysis on daily bases and rectify themselves wherever falling short in their own eyes. We need to learn that trying to fit in other’s shoes. One may often get sores in his or her own feet. Each of us has a unique personality and must be respected as such. To many doses of intellectuality from others may have a paralyzing effect on your own mental being. Therefore preserve what lies embedded within you and yes! assimilate from others whatever good lies there to be assimilated.</p>

<p>The Almighty God has bestowed wings to all the birds but the art of embarking upon the flight and the distance to be covered lies in their own hands. Physical abilities are the wings imparted by Him to us and the height of our castle will surely be as dreamt by us.</p>

<p>'God, give me the serenity to accept the things I can’t change, the perseverance to change the things I can and the wisdom to know the difference'</p>
